    foreach ($joue as $j) {
        if ($j['idActeur'] == $id) {
            $f = $films[$j['idFilm'] - 1];
            $personne->acteurs[] = new Film($f['titre'], $f['annee'], $f['nbSpectateurs'],  $f['idRealisateur'],$f['id']);
        }
    }
    return $personne;
}


