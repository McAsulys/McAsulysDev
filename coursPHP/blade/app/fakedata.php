    $data["films"] = array();

    foreach ($films as $f) {
        if($f['id']==26 || $f['id']==30)  continue;
        $data["films"][] = new Film($f['titre'], $f['annee'], $f['nbSpectateurs'], $f['idRealisateur'], $f['id']);
    }
